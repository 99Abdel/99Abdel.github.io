Date.prototype.get_calendar_code_index = function()
{
   var calendar_code_index = 0 ;

   var this_year = this.getFullYear() ;

   if ( this_year > 2000 && this_year <= 2400 )
   {
      calendar_code_index = this_year - 2001 ;
   }
   else if ( this_year > 1600 && this_year <= 2000 )
   {
      calendar_code_index = this_year - 1601 ;
   }
   else if ( this_year > 1200 && this_year <= 1600 )
   {
      calendar_code_index = this_year - 1201 ;
   }
   else if ( this_year > 800 && this_year <= 1200 )
   {
      calendar_code_index = this_year - 801 ;
   }
   else if ( this_year > 400 && this_year <= 800 )
   {
      calendar_code_index = this_year - 401 ;
   }
   else if ( this_year > 0 && this_year <= 400 )
   {
      calendar_code_index = this_year - 1 ;
   }
   else if ( this_year > 2400 && this_year <= 2800 )
   {
      calendar_code_index = this_year - 2401 ;
   }
   else if ( this_year > 2800 && this_year <= 3200 )
   {
      calendar_code_index = this_year - 2801 ;
   }
   else
   {
      console.log( "\n get_calendar_code_index(): Year cannot be handled. \n" ) ;
   }

   return calendar_code_index ;
}  ;

Date.prototype.is_last_day_of_month = function()
{
   var calendar_code = calendar_codes[ this.get_calendar_code_index() ] ;

   var days_of_this_month =

          gregorian_calendars[ calendar_code - 1 ] [ this.getMonth() ] ;

   return ( days_of_this_month.length == this.getDate() ) ;
} ;

Date.prototype.is_leap_year = function()
{
   var calendar_code = calendar_codes[ this.get_calendar_code_index() ] ;

   return ( calendar_code > 7 ) ;
} ;


Date.prototype.index_for_day_of_week = function() 
{
   //  day_index will get a value in the range from 0 to 6,
   //  0 meaning Monday and 6 meaning Sunday.

   var calendar_code = calendar_codes[ this.get_calendar_code_index() ] ;

   var day_code_for_this_day =

          gregorian_calendars[ calendar_code - 1 ]
                             [ this.getMonth() ] [ this.getDate() - 1 ] ;
                  
   return  ( day_code_for_this_day - 1 ) ;
} ;

Date.prototype.get_day_of_week = function()
{
   var days_of_week  =  [ "Monday", "Tuesday", "Wednesday",
                          "Thursday", "Friday", "Saturday", "Sunday" ] ;

   return  days_of_week[ this.index_for_day_of_week() ] ;
} ;

Date.prototype.plus_one_day = function()
{
   if ( this.is_last_day_of_month() )
   {
      this.setDate( 1 ) ;

      if  (  this.getMonth()  <  11  )
      {
         this.setMonth( this.getMonth() + 1 ) ;
      }
      else
      {
         this.setMonth( 0 ) ;  // January
         this.setFullYear( this.getFullYear() + 1 ) ; // Next year
      }
   }
   else
   {
      this.setDate( this.getDate() + 1 ) ;
   }
} ;

Date.prototype.minus_one_day = function()
{
   if ( this.getDate()  >  1  )
   {
      this.setDate( this.getDate() - 1 ) ;
   }
   else
   {
      //  We have to decrement from the first day of month.

      if (  this.getMonth() == 0 )
      {
         // It is January. We must go to the last day of previous year.

         this.setDate( 31 ) ;
         this.setMonth( 11 ) ; // December
         this.setFullYear( this.getFullYear() - 1 ) ; // Previous year
      }
      else
      {
         // We must go to the last day of previous month in this year.

         var calendar_code = calendar_codes[ this.get_calendar_code_index() ] ;

         this.setMonth( this.getMonth() - 1 ) ;

         var days_of_previous_month =

                    gregorian_calendars[ calendar_code - 1 ] [ this.getMonth() ] ;

         this.setDate( days_of_previous_month.length ) ; // set last day
      }
   }
} ;

Date.prototype.get_distance_to = function( another_date )
{
   var years_of_distance, months_of_distance, days_of_distance ;
   var start_date, end_date ;
   var start_day, end_day ;

   if ( this.is_earlier_day_than( another_date ) )
   {
      start_date  =  this ;
      end_date    =  another_date ;  
   }
   else
   {
      start_date  =  another_date ;
      end_date    =  this ;
   }

   //  We will suppose that day 30 is the last day of every
   //  month. This way we minimize calculation errors.
   
   //  Leap year February 28 will also considered as 
   //  a last day of monty.

   if ( start_date.is_last_day_of_month() ||
        ( start_date.getDate()   ==  28  &&
          start_date.getMonth()  ==  1  ) )
   {
      start_day   =  30  ;
   }
   else
   {
      start_day   =  start_date.getDate() ;
   }

   if ( end_date.is_last_day_of_month() ||
        ( end_date.getDate()   ==  28  &&
          end_date.getMonth()  ==  1  ) )
   {
      end_day   =  30  ;
   }
   else
   {
      end_day   =  end_date.getDate() ;
   }

   years_of_distance  =  end_date.getFullYear() - start_date.getFullYear() ;
   months_of_distance =  end_date.getMonth() - start_date.getMonth() ;
   days_of_distance   =  end_day  -  start_day ;

   if ( days_of_distance  <  0 )
   {
      months_of_distance  --  ;
      days_of_distance  =  days_of_distance  +  30 ;
   }

   if ( months_of_distance  <  0 )
   {
      years_of_distance   --  ;
      months_of_distance  =  months_of_distance +  12 ;
   }

   // This method delivers data to its caller in a JavaScript object.

   var distance_to_caller = 
   {
      years: years_of_distance,
      months: months_of_distance,
      days: days_of_distance
   } ;
   
   return distance_to_caller ;
} ;

